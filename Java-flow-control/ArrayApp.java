
public class ArrayApp {

	public static void main(String[] args) {

		String[] users = new String[3];
		users[0] = "jjo";
		users[1] = "jsj";
		users[2] = "jua";
		
		System.out.println(users[1]);
		System.out.println(users.length);
		
		int[] scores = {10, 11, 12};
		System.out.println(scores[1]);
		System.out.println(scores.length);
		
	}

}
